# Stephen Flynn Revised Loop Counter For Numbers
# Works by using the X unknown variable and putting it into the range of numbers to print. By using a print statement printing the numbers stored in that variable.
for x in range(5):
    print(x)

# Prints out 3,4,5
for x in range(3, 6):
    print(x)

# Prints out 3,5,7
for x in range(3, 8, 2):
    print(x)
